public class Coordinate
{
	public int Top;
	public int Bottom;
	public int Left;
	public int Right;
	
	public Coordinate()
	{
		Top = 0;
		Bottom = 0;
		Left = 0;
		Right = 0;
	}
	
	public Coordinate(int top, int bottom, int left, int right)
	{
		Top = top;
		Bottom = bottom;
		Left = left;
		Right = right;
	}
}
